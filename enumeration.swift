//In Swift, an enum (short for "enumeration") is a data type that defines a set of related values in a 
// type-safe way. It's used to represent a fixed set of options or states, often making code more readable, 
// expressive, and less error-prone.

//Basic Syntax:
//Here's a simple example of an enum in Swift:

enum Direction
case north
case south
case east 
case west

//In this example, Direction is an enum that can hold one of four possible values: .north, .south, .east, or .west.